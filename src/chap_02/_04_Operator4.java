package chap_02;

public class _04_Operator4 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c = true;

        System.out.println(a || b || c);
        System.out.println(a && b && c);

        System.out.println((5 > 3) && (3 > 1));
        System.out.println((5 > 3) && (3 < 1));

        System.out.println(!true);


    }
}
