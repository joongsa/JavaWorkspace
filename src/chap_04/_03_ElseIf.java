package chap_04;

public class _03_ElseIf {
    public static void main(String[] args) {
        boolean halla = false;
        boolean mango = false;

        if(halla){
            System.out.println("한라봉");
        }else if(mango){
            System.out.println("망고");
        }else{
            System.out.println("아아+1");
        }
        System.out.println("주문완");
    }
}

